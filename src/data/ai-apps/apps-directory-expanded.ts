// Helper functions
export const getAppsByCategory = (category: AICategory): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.category === category);
};

export const getHotApps = (): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.isHot);
};

export const getPremiumApps = (): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.isPremium);
};

export const getFeaturedApps = (): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.isFeatured);
};

export const getNewApps = (): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.isNew);
};

export const searchApps = (query: string): AIApp[] => {
  const lowercaseQuery = query.toLowerCase();
  return aiAppsDirectoryExpanded.filter(app =>
    app.name.toLowerCase().includes(lowercaseQuery) ||
    app.description.toLowerCase().includes(lowercaseQuery) ||
    app.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getAppsByPricing = (pricing: 'free' | 'freemium' | 'paid'): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.pricing === pricing);
};

export const getTopRatedApps = (minRating: number = 4.5): AIApp[] => {
  return aiAppsDirectoryExpanded.filter(app => app.rating && app.rating >= minRating);
};
